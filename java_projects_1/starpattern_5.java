// Write a java program to find the following pattern:

//    *
//   ** 
//  ***
// **** 

package java_projects_1;

public class starpattern_5 {
    public static void main(String[] args) {
        int row =5;
        for(int i=0; i<row; i++)
            {
            for(int j=0; j<=i; j++){
                System.out.print("* ");
            }
                System.out.println();
            }    
    }
}



















